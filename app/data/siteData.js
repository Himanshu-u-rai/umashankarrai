// Re-export contact greeting helpers so other components don't recompute.
export const whatsappLink = (greetingText) => {
  if (!hasWhatsApp) return "#contact";
  const digits = advisor.whatsapp.replace(/\D+/g, "");
  const text = encodeURIComponent(greetingText ?? "");
  return `https://wa.me/${digits}${text ? `?text=${text}` : ""}`;
};

export const telLink = () => (hasPhone ? `tel:${advisor.phone}` : "#contact");
export const mailtoLink = (subject = "", body = "") => {
  const params = [
    subject ? `subject=${encodeURIComponent(subject)}` : "",
    body ? `body=${encodeURIComponent(body)}` : "",
  ]
    .filter(Boolean)
    .join("&");
  return `mailto:${advisor.email}${params ? `?${params}` : ""}`;
};
